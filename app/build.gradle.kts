tasks.register("checkBraces") {
  doLast {
    val file = file("src/main/java/com/example/ui/YourDocApp.kt")
    var openCount = 0
    var closeCount = 0
    var insideString = false
    val lines = file.readLines()
    var prevDiff = 0
    for (i in lines.indices) {
      val line = lines[i]
      var j = 0
      while (j < line.length) {
        val c = line[j]
        if (c == '"' && (j == 0 || line[j-1] != '\\')) {
          insideString = !insideString
        }
        if (!insideString) {
          if (c == '/' && j < line.length - 1 && line[j+1] == '/') {
            break
          }
          if (c == '{') openCount++
          if (c == '}') closeCount++
        }
        j++
      }
      val diff = openCount - closeCount
      if (diff != prevDiff) {
        if (i >= 620 && i < 740) {
          println("Line ${i+1}: Diff changes $prevDiff -> $diff | ${line.trim()}")
        }
        prevDiff = diff
      }
    }
  }
}
